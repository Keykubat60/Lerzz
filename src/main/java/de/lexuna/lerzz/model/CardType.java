package de.lexuna.lerzz.model;

/**
 * The CardType enumeration represents the different types of cards that can be used in the game.
 *
 * Currently, the only value in this enumeration is SIMPLE. This value is used to represent a simple
 * card with a question and answer.
 *
 * Enumerations are a special type of Java class that represent a fixed set of values. In this case,
 * the CardType enumeration is used to ensure that only valid types of cards are used in the game.
 * Additional values can be added to this enumeration if needed in the future.
 */
public enum CardType {
    /** A simple card with a question and answer. */
    SIMPLE
}
